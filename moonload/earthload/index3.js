dotsContainer.style.display = 'flex';
dotsContainer.style.justifyContent = 'center';
dotsContainer.style.alignItems = 'center';
dotsContainer.style.position = 'absolute';
dotsContainer.style.top = '50%';
dotsContainer.style.left = '50%';
dotsContainer.style.transform = 'translate(-50%, -50%)';
dotsContainer.style.zIndex = '10';
document.body.appendChild(dotsContainer);

for (let i = 0; i < 3; i++) {
  const dot = document.createElement('div');
  dot.style.width = '45px';
  dot.style.height = '45px';
  dot.style.background = 'linear-gradient(to bottom, black, white)';
  dot.style.borderRadius = '50%';
  dot.style.margin = '0 10px';
  dot.style.animation = `jump 1s ease-in-out infinite ${i * 0.2}s`;
  dotsContainer.appendChild(dot);
}

